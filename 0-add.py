#!/usr/bin/env python3
"""A module that defines the `add` function using the type annotation."""

def add(a: float, b: float) -> float:
    """Returns the addition of two numbers.

    Args:
        a: The first number to be added.
        b: The second number to be added.

    Returns:
        The result of the addition of argument `a` and `b`.
    """
    return (a + b)
